// Helper to create platforms
const createPlatform = (
  x: number,
  y: number,
  width: number,
  height: number,
  type: Platform['type'] = 'normal'
): Platform => ({
  x,
  y,
  width,
  height,
  type,
});

// Helper to create enemies
const createEnemy = (
  x: number,
  y: number,
  type: Enemy['type'],
  hp: number = 1,
  speed: number = 1
): Enemy => ({
  x,
  y,
  width: type === 'loneliness-monster' ? 80 : type === 'storm-cloud' ? 50 : 30,
  height: type === 'loneliness-monster' ? 80 : type === 'storm-cloud' ? 40 : 30,
  type,
  hp,
  maxHp: hp,
  animationFrame: 0,
  direction: 1,
  speed,
});

// Helper to create collectibles
const createCollectible = (
  x: number,
  y: number,
  type: Collectible['type'],
  frameIndex?: number,
  text?: string
): Collectible => ({
  x,
  y,
  width: 24,
  height: 24,
  type,
  collected: false,
  animationFrame: 0,
  frameIndex,
  text,
});

